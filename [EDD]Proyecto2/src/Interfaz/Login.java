    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Usuario = jTextField2.getText().trim();
        String pass = jPasswordField1.getText().trim();

        if (!"".equals(Usuario) && !"".equals(pass)) {
            if (RU.isSelected()) {
                System.out.println("Usuario");
                Clave us = normal.BuscarUser(Usuario);
                String encriptacion = SHA.getSHA256(pass);
                if (us != null) {
                    if ((((Usuario) us.getValor()).getPass()).equals(encriptacion)) {
                        en = (Usuario) us.getValor();
                        setVisible(false);
                        new User().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(null, "Acceso Denegado, contrase√±a incorrecta", "Error Login", JOptionPane.ERROR_MESSAGE);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Acceso Denegado, El usuario no ha sido encontrado", "Error Login", JOptionPane.ERROR_MESSAGE);
                }
            } else if (RP.isSelected()) {
                System.out.println("Piloto");
                Clave us = conductor.BuscarUser(Usuario);
                String encriptacion = SHA.getSHA256(pass);
                if (us != null) {
                    if ((((Usuario) us.getValor()).getPass()).equals(encriptacion)) {
                        setVisible(false);
                        new User().setVisible(true);
                        
                    } else {
                        JOptionPane.showMessageDialog(null, "Acceso Denegado, contrase√±a incorrecta", "Error Login", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Acceso Denegado, El usuario no ha sido encontrado", "Error Login", JOptionPane.ERROR_MESSAGE);
                }
            } else if (RA.isSelected()) {
                System.out.println("Administrador");
                if (("marvin_martinez").equals(Usuario) && ("admin").equals(pass)) {
                    setVisible(false);
                    new Admin().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Acceso Denegado, Usuario o contrase√±a incorrecto", "Error Login", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "No se ha seleccionado el tipo de usuario", "Login", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Acceso Denegado, Usuario o contrase√±a vacias", "Error Login", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

